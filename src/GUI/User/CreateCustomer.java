    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtName.getText().isEmpty() || txtEmail.getText().isEmpty() || txtPassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "All field is required !");
        } else {
            String name = txtName.getText();
            String email = txtEmail.getText();
            String password = txtPassword.getText();


            User u = new User();
            u.setEmail(email);
            u.setName(name);
            u.setPassword(password);
            u.setRole(u.CUSTOMER_ROLE);
            if (u.create()) {
                Customers c = new Customers();
                c.setUser(new User().where("email", email));
                if (c.create()) {
                    JOptionPane.showMessageDialog(null, "Created !");
                } else {
                    JOptionPane.showMessageDialog(null, "Position or Salary Validation Error!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Email or Name Validation Error!!");
            }        // TODO add your handling code here:
        }
    }//GEN-LAST:event_jButton1ActionPerformed

