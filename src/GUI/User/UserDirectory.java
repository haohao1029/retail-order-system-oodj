    private void initTable() {
        ArrayList<User> users = new User().all();
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //lamda expression... cool right hahah
        users.forEach((user) -> {
            switch (user.getRole()) {
                case "admin": {
                    Admin staff = new Admin().where("user_id", String.valueOf(user.getId()));
                    model.addRow(new Object[]{
                        user.getId(),
                        user.getName(),
                        user.getEmail(),
                        user.getRole(),
                        staff.getSalary(),
                        staff.getPosition()
                    });
                    break;
                }
                default:
                    //Just Incase
                    model.addRow(new Object[]{
                        user.getId(),
                        user.getName(),
                        user.getEmail(),
                        user.getRole()
                    });
                    break;
            }
        });
    }

