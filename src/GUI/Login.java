    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        boolean proceed = true;
        String email = txtEmail.getText();
        String password = txtPassword.getText();

        if (!valid.isValidEmail(email)) {
            help.showWarningBox("Please Enter a valid Email");
            proceed = false;
        }
        if (!valid.isValidString(password)) {
            help.showWarningBox("Password Should not be empty or contain \",\" ");
            proceed = false;
        }

        if (proceed) {
            User u = new User();
            u.setEmail(email);
            u.setPassword(password);
            if (u.login()) {
                if (u.isAdmin()) {
                    new ManageOrders().setVisible(true);
                    this.dispose();
                } else {
                    //TODO
                    new CustomerProducts().setVisible(true);
                    this.dispose();
                }
            } else {
                help.showMessageBox("Email or Password do not match");
            }
        }


    }//GEN-LAST:event_btnLoginActionPerformed

